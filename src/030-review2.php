<B><U><FONT SIZE=4><P ALIGN="CENTER">Rbots 0.30</P></FONT></U></B>
<P ALIGN="CENTER">Review by <A HREF="mailto:epj_backslash@hotmail.com">Backslash</A></P>

<B><U><P>Rbots and Guns: NRA Members, or JK Newbs?</B></U></P>

<p>So, what can I say, I loaded up Rbots, and decided I'd start on my weakest variation of the Jedi Knight Multiplayer: Guns. I picked Canyon Oasis, and started to run. 4 Rbots joined the fray, from Boba Fett to Jerec. I spawned next to the Rail Detonater, and started fragging the blaster-wielding bots right away. Within about five mins, I went into my controls, and rebound the key for AI Skill. So, they went from Newbie to Experienced. I started to see them shine, and they got me more than a few times with the Rail Detonater. Then I remembered that sweet weapon called the Concussion Rifle. So, I made a run for it, and grabbed it. Got my ammo count up, then as I chased Jerec into the water, I got a surprise. I expected him to just swim around, but he did the exact opposite. He jumped out of the water, turned, and blew me up with the Rail Detonater. Then came the reign of Rbot + Conc Rifle. I started dying more and more. So I finally got the conc back myself, and turned the AI up to full. I played for about another 15 mins. The highest scoring bot had something like 20, I had about 45. Defiantly fun. Another thing about the bots I noticed however, was the fact that if I used Force Pull on them, say, on their Bryar pistol, they could keep shooting with it. While it still scared me, I pulled out my saber, and was glad to see them return the favor by swinging back. Good improvments on aim, and the fighting/dodging skills.</p>

<B><U><P>Teams? Theres no Rbot in team, or is there?</B></U></P>

<p>Now I was a little confused by the idea of teaming up with the Rbots. After NOT making a game properly, I got the hang of being on a team with bots. So, myself and Lightside opposed the evil Juztyn and GuNbOy (For the record, the JuzBot sucks compared to the real Juztyn). So, once again we trudged through the Oasis of Canyons. After about a half hour of blasting eachother, I had been blown up by my teammate a few times, and we had dominated over Juz and Gunny. While I died by my own team a few times, the Rbots definatly have the hang of teamplay.</p>

<B><U><P>There is no Rbot named Darth Maul.</B></U></P>

<p>Sabers. This is what I play in JK. So I took up BGJ Saber FFA. So, I pulled out my saber, and started to run around. In seconds, 4 wannabe jedis joined the fray. I almost laughed, as soon as they appeared, they dissapeared. In a 10 min game against on Inhuman, I had a score of 50, and had died maybe 3 times. Personally, I like the idea of having a saberist bot, but this one doesn't seem to be competant enough to fight. They'd constantly jump off the cliffs in BGJ, run near the walls, jump like a rabbit. And heck, they acted like most Zoners, they ran for health packs every time I hit them and they didn't die. In all, they're a good start, but need improvement.</p>

<B><U><P>Overview</B></U></P>

<ul>
<li>They like the Lightning Traps in CO.
<li>They can swim, jump, and shoot like heck.
<li>They can't fight with a saber for anything.
</ul>
In all, its a great job by the whole team!




