<center><h2>What is Rbots?</h2></center>

<p>
Rbots is a project to create a 'bot' for Jedi Knight. A bot is a computer controlled player for multiplayer games. It should react and play just like a human player - it has health, shields, limited ammo & weapons and when it dies it will 'respawn' to allow the game to continue. Multiple bots will battle amongst themselves as well as any number of human players. 
</p>
<p>
Rbots is a 'community project', where anyone and everyone has full access to the latest source code and is encouraged to contribute. No one has to join the team full time, just sending in little snippets of code or a single level that's been converted, when they get time. <a href="mailto:raynar@gamebox.net">Raynar</a> co-ordinates the project, updating the main source and rereleasing it as required.</p>
<p>The actual cog code is now fully public domain. Anyone can use it or any parts of it however they like, in whatever project they like. Any code contributed to the project is also available in the same way.
</p>

<h3 align="center">Features</h3>
<p>
<ul>
<li>Full lightsaber support with glowsabers
<li>Supports up to 32 bots per level
<li>Comes complete with 18 different bot models (skins) (16 deathmatch & 2 teamplay)
<li>Each level comes ready for action with 16 bots available
<li>3 different gameplay modes - Deathmatch, Sabers & Teamplay
<li>5 difficulty settings - set via hotkey
<li>Add/remove bots via hotkey
<li>Botcam - hotkeyable camera mode which will follow each bot around the level
<li>Bot Scoring system - keeps track of scores for the bots
<li>Special path node properties - allows bots to do extra things like activate switches, jump over ledges, lay mines etc.
<li>Intelligent weapon selection - bots will now change weapons based on current situation, rather than just using the 'biggest' gun it has
<li>Secondary fire - bots now use secondary fire for concussion rifle & railgun
<li>Bots will dodge incoming projectiles based on difficulty
</ul>

<h3 align="center">MotS only features</h3>
<p>
<ul>
<li>Drastically reduced file size
<li>Comes complete with all 55 multiplayer bot models for MotS
<li>32 bots are available for use with all levels
</ul>

<h3 align="center">What do you want me to do?</h3>
<p>
Run Rbots as much as you like. I need as much feedback as possible before Rbots can go to Beta release. Please post your bug reports on the <a href="http://forums.massassi.net/vb3/forumdisplay.php?f=11" name="Cog Forum" title="Cog Forum">Cog Forum</a>. If you feel you can contribute to the project, head over to the <a href="/problems.php" name="Work required" title="Work required">Work required</a> page to see what needs to be done.</p>

<h3 align="center">Can I use Rbots in my own level?</h3>
<p>
Follow through the tutorial and download the Rbots SDK. Together they will allow you to add Rbots to any multiplayer level.</p>
<p>
Anyone who would like to use Rbots code in their level, PLEASE at least mention the origins of the code and acknowledge the Rbots project in your level's 'readme' file. You may use any or all of the Rbots project code in your level, modified in any way you see fit, as long as you follow the above.</p>

