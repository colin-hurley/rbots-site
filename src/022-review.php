<B><U><FONT SIZE=4><P ALIGN="CENTER">Rbots 0.22: Enough is Enough</P></B></U>
<P ALIGN="CENTER">Review by <A HREF="mailto:dark_jedimaster_99@yahoo.com">Darkheart</A></P>

<P>Well folks it finally happened!  After months of taking abuse from thousands of JK Players the Rbots are getting revenge!  I had heard a little about the new Rbots and decided it was time to download it.  While a little large I waited the 31 mins 45 seconds to download it (28.8 connection, what can you do?) and found it was well worth it!  Death is the name of the Rbots game and they have learned how to kill quite well.  They are now harder to kill and a lot of times get the one up on you.  There are now 3 levels of play that I will cover in my review of the Rbots: DeathMatch, Team-Play, and Sabers.  I have screen shots of my matches with the Rbots to show how good they really are.</P>

<B><U><P>DeathMatch:</P></B></U>
</FONT>

<P><IMG SRC="images/review/022-review1.gif" WIDTH=427 HEIGHT=266></P>

<FONT SIZE=4><P>Nothing like a good old rail dent flying towards your head.  Now with the old bots you knew you were going to win no matter what.  Also the score wasn't kept so you have to reach a high number of kills before you saw anything.  But now the score is always there and then the Rbots make a kill you see who and their score.  Also it seems they know just the right time to come and kill you!  I was busy taking out one of the bots and down to low health when he finally died, as I turned around *bam* a rail dent to the face and I am dead.  Quite a blow to the ego when a few months ago I could play a good hour without getting killed.</P>

</FONT><FONT SIZE=2><P><IMG SRC="images/review/022-review2.gif" WIDTH=426 HEIGHT=266></P></FONT>

<B><U><FONT SIZE=4><P>Team-Play:</P></B></U>
<P>Yup now there is team play!!!!  And yes this is worse then the DeathMatch.  Now one thing I have found is, a conc totting Rbot is nothing to laugh out.  (Look above)  They have utilized the secondary fire so there a fewer self-kills with it.  Team play is fun, because now you have someone watching your back.  One small problem is the bots seem to lose a little of the edge they have in DeathMatch mode, but they are still kicking butt.  As we can see Rbots with guns are something to look out for, but how are they with sabers?</P>

</FONT>
<FONT SIZE=2><P><IMG SRC="images/review/022-review3.gif" WIDTH=426 HEIGHT=266></P></FONT>
<B><U><FONT SIZE=4><P>Sabers:</P></B></U>

<P>Well, how can I put this, the Rbots need a little more Jedi Training before they can be consider full Jedi.  Their saber skills are lacking, but it is a great first attempt.  It takes a long time to kill or be killed in this level.  But the Saber Swinging Rbots make great screen shots huh?  I think if given a little more time the Rbots Sabers will be the best of the best!</P>

<B><U><P>Closing:</P></B></U>

<P>Yet another great release of Rbots.  Raynar & Hell Raiser, have again shown great talent in the Rbots 0.22 release.  After playing your going to wonder, "was it bots or did someone link up to my computer and play me?"  Also, I wonder what can these 2 great people do to top what they have already?  I think we shall see very soon!  Also I would like to point out that LucasArts should have thought about doing this, because it is a great thing when Zone is raided with hackers and you have nobody to play with.  I think Rbots will soon replace the good old LAN Party because they don't get mad, they can play anytime, and the best part: THEY DON'T TALK BACK!  For OuterRim Network this is Darkheart signing off.</P>

