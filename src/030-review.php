<B><U><FONT SIZE=4><P ALIGN="CENTER">Rbots: If Once Wasn't Enough!</P></FONT></U></B>
<P ALIGN="CENTER">Review by <A HREF="mailto:dark_jedimaster_99@yahoo.com">Darkheart</A></P>

<p>A couple months ago I reviewed Rbots version 22.  Now they had come a long way from what they were to version 22 and I thought there is no way they could get any better.  Well, to put it the Obi Wan way "I was wrong."  If seems that with each new version the Rbots become better and better.  So on with the review.</p>

<B><U><P>Rbots With Guns: Rambo Time Baby!</B></U></P>

<p>With each new version of Rbots there seems to be an improvement on their gun skills.  I am a gunner and I am begining to see that the Rbots are catching up with some of the better gun slingers on the Zone.  They actully follow you into the water and if that doesn't suprise you the fact that they can shoot you in the water will.  I had no time to get to the conc because of this.  Also they now use the secondary function on their weapons.  I once read that the best way to kill and enemy is with a rail dent stuck to their face.  Well the Rbots must have been to the same place because 3 times I was hit with a sticky rail!  I also noticed their weapon on choice is the repeater.  Don't ask me why, but that what I got hit with the most.</p>

<B><U><P>You/Rbot vs Rbot/Rbot</B></U></P>

<p>Ah yes there is team play with these guys.  Lets just say it is nice to have someone on your side.  The Rbots can be dirty fighters, but they get their butts kicked when there is someone with you.  I find that team play is just as good as the death match if not better.  The ability to add/remove bots also makes this more enjoyable.  So in short they follow everything they did in Deathmatch, but this time with teams.</p>

<B><U><P>Rbots with Sabers: Go Back and Get Some Training!</B></U></P>

<p>In my last review I talked about the Rbots with Sabers and how it was a nice feature, but needed work.  Well they worked on it and it is better then before, but it is also worse.  The bots now actully fight you instead of going in the endless circle path.  But they are horrible in sabers and its like playing the newest of newbies on the Zone.  In the tower they jumped off the bridge into the center taking most of their health.  They also get stuck in the crates.  One other thing is that 2 of the bots just kinda disappeared.  (I didn't remove them)  Now at Battleground they were a little better getting 2 kills on me, but they still had the same problems.  I believe they either need a new master or should just give up on being Jedi and become soldiers.</p>

<p>First off, I would like to thank the Rbot staff for making this awesome mod.  You have boosted the self-image of us bad JK players.  Second, you have done a great job on making them better at guns, but I think the next version should cover sabers.  Third, please for the love of god get some new levels.  I would be happy to make a half-decent level for ya.  The LEC levels are sucking and you can't get the full use of the Rbots with them stuck in LEC's crap.  Fourth, please check the following things:  CO's lightning trap is taking a lot of health, rail dent would kill with one shot at times, sabers would kill with one swing at times.  On another good note, the Rbots have also found out how to jump and they do it well.  This is the end of my review.</p>

<p>Sincerely,
Darkheart (Blind_Mr_Smith also is me)</p>

