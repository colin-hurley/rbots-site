<a name="q1"></a><h3>When are you going to add 'your suggestion' to Rbots?</h3>
<p>
The answer is simple. There will be no further modifications until the basic functions and features are working perfectly.
Check the <a href="development.php">Development Priority List</a> to see what stage the development is up to. Once this is done, then I'll consider any further changes. Don't be discouraged about making suggestions for things to add via the <a href="http://forums.massassi.net/vb3/forumdisplay.php?f=11" name="Cog Forum" title="Cog Forum">Cog Forum</a>, but just don't expect them in the next release. 
</p>
<a name="q2"></a><h3>What is the 'SDK'?</h3>
<p>
The SDK is the Software Development Kit. This together with the <a href="tutorial/tutorial.html">tutorial</a> allows you to create your own Rbots level. This could be a brand new level you have created or an old level you wish to convert. Either way, the Rbot SDK is what you need.
</p>
<a name="q3"></a><h3>Can I use other mods with Rbots?</h3>
<p>
You can use other mods, however the bots will still use the standard JK weapons unless the mod has been specifically designed to work with Rbots. Other mods may also conflict with the hotkeys that Rbots uses.
</p>
<a name="q4"></a><h3>What does the 'R' stand for in 'Rbots'?</h3>
<p>
Would you believe 'R' for 'Raynar'? When Rbots was in very early development, there was actually 2 bot projects. I just named mine Raynar's Bots, or Rbots for short.
</p>
<a name="q5"></a><h3>Is there any difference between the JK & MotS versions?</h3>
<p>
You may have noticed that the JK version is considerably larger to download and install. The JK engine does not allow certain functions that the MotS version does. In essence, the JK version requires a separate model of every character holding every weapon, leading to the huge file size. The MotS version has no such restrictions. Apart from this one major distinction, both verions should run the same as far as the players are concerned.
</p>
<a name="q6"></a><h3>Why don't you make it that the bots can work in any level?</h3>
<p>
It's just not possible to create intelligent bots that can be added to any level. Other bots have been created in the past that attempt to do this, but without much success. The bots need to be told how to move from one part of a level to another. Can you imagine a bot working out how to get to the conc rifle in Canyon Oasis?  Cog is severely limited, and the current development is already pushing those limits as it is. In short, the answer is no. Rbots will stay using the path node system, which means that each level will need to be manually modified before the bots can be used.
</p>
<a name="q7"></a><h3>How can I add different skins for the bots?</h3>
<p>
You can only change the skins (models) for the bots if you create your own Rbots level. This is not something that can be changed after the level has been 'gobbed'. Please refer to <a href="tutorial/tutorial-2.html">Tutorial 2</a> for details on making your own Rbots characters.
</p>
<a name="q8"></a><h3>Where can I get more Rbots levels?</h3>
<p>
I haven't created any more levels for Rbots yet. The SDK is available to download so you can create your own if you wish.

